package com.woliao.model;

public class Prop {
	private int id;
	
	private String username;
	
	private int deng;
	
	private int ka;
	
	private int score;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public int getDeng() {
		return deng;
	}

	public void setDeng(int deng) {
		this.deng = deng;
	}

	public int getKa() {
		return ka;
	}

	public void setKa(int ka) {
		this.ka = ka;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}
	
}
